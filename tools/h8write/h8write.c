int select_device(char *devcode) {
	unsigned char	sum;
	int		i, s;

	sum = 0x10;
	putbyte(0x10);
	sum += 4;
	putbyte(4);
	for(i = 0;i < 4;i++) {
		sum += devcode[i];
		putbyte(devcode[i]);
	}
	sum = 0 - sum;
	putbyte(sum);
	s = getbyte() & 0xff;
	bytecheck(s, 0x06);
}

