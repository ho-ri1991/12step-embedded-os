int set_rate(int bitrate, int freq) {
	unsigned char	sum;
	int		i, s;

	sum = 0x3f;
	putbyte(0x3f);
	sum += 7;
	putbyte(7);
	putbyte(0);
	sum += bitrate;
	putbyte(bitrate);
	sum += freq >> 8;
	putbyte(freq >> 8);
	sum += freq & 0xff;
	putbyte(freq & 0xff);
	sum += 1;
	putbyte(1);
	sum += 1;
	putbyte(1);
	putbyte(0);
	sum = 0 - sum;
	putbyte(sum);
	s = getbyte() & 0xff;
	bytecheck(s, 0x06);
}

unsigned char put_long(int addr, unsigned char _sum) {
	unsigned char c, sum;

	sum = _sum;
	c = (addr >> 24) & 0xff;
	putbyte(c); sum += c;
	c = (addr >> 16) & 0xff;
	putbyte(c); sum += c;
	c = (addr >> 8) & 0xff;
	putbyte(c); sum += c;
	c = addr & 0xff;
	putbyte(c); sum += c;
	return sum;
}

